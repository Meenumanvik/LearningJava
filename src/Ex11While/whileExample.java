package Ex11While;

public class whileExample {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
        System.out.println("End of loop");
        //  I, C, U
        for (int j = 0; j < 10 ; j++) {
            System.out.println(j);
        }
    }
}
